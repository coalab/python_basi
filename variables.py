# This file provides an example of variable declaration and usage in Python, illustrating how to store and manipulate data.

# Example of variable declaration
name = "Alice"
age = 14
grade = "8th"

# Print the variables
print("Student Name:", name)
print("Student Age:", age)
print("Student Grade:", grade)

# Example of manipulating variables
age_next_year = age + 1
print("Age next year:", age_next_year)

# Example of using variables in a sentence
print(f"{name} is currently in {grade} grade and will be {age_next_year} years old next year.")